package com.eomcs.basic.ex04;

public class Exam21 {
  public static void main(String[] args) {

    // 정수 변수의 메모리 크기

    byte b; // 1바이트 0000 0000 ~ 1111 1111 = 00 ~ ff = 0 ~ 255 => -128 ~ 127
    b = -129;
    b = 127;

    short s; // 2바이트 : -32768 ~ 32767
    s = -32768;
    s = 32767;

    int i; // 4바이트 : 약 -21억 ~ 21억
    i = -21_0000_0000;
    i = 21_0000_0000;

    long l; // 8바이트 : 약 -922경 ~ 992경
    l = -922_0000_0000_0000_0000L;
    l = 922_0000_0000_0000_0000L;
    

  }
}